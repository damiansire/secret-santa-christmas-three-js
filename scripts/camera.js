function ponerCamaraDeFrente(camera) {
  camera.position.z = 4;
  camera.position.y = 1;
  camera.position.x = 2;
  camera.rotation.x = 0;
  camera.rotation.z = 0;
  camera.rotation.y = 0;
}

function ponerCamaraDeArriba(camera) {
  camera.position.z = 4;
  camera.position.y = 5;
  camera.position.x = 2;
  camera.rotation.x = -1;
  camera.rotation.z = 0;
  camera.rotation.y = 0;
}
